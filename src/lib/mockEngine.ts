// ───────────────────────── публичный API (заменяется на реальный Claude API) ─────────────────────────

const FORCE_LABEL: Record<Force, string> = {
  push: 'Боль (Push)',
  pull: 'Магнит (Pull)',
  inertia: 'Лёгкость (Inertia)',
}

function delay<T>(value: T, ms: number): Promise<T> {
  return new Promise((resolve) => setTimeout(() => resolve(value), ms))
}

export function analyzeIdea(idea: string): Promise<Analysis> {
  const seed = hashString(idea.trim().toLowerCase())
  const rng = mulberry32(seed)
  const short = shortIdea(idea)

  const forces: ForceScore[] = (['push', 'pull', 'inertia'] as Force[]).map((force) => {
    const score = 1 + Math.floor(rng() * 5) // 1..5
    return {
      force,
      label: FORCE_LABEL[force],
      score,
      text: pick(rng, ROAST[force][tier(score)]),
    }
  })

  const avg = forces.reduce((sum, f) => sum + f.score, 0) / forces.length
  const score = Math.min(5, Math.max(1, Math.round(avg * 10) / 10))
  const verdict = pick(rng, VERDICTS[tier(Math.round(score))])

  const analysis: Analysis = {
    score,
    verdict,
    forces,
    offers: buildOffers(short),
    questions: QUESTIONS.map((q) => q(short)),
  }

  return delay(analysis, 1200)
}

export function pivotIdea(idea: string, analysis: Analysis): Promise<PivotResult> {
  const short = shortIdea(idea)
  // Бьём в самую слабую силу — её и докручиваем в первую очередь.
  const weakest = [...analysis.forces].sort((a, b) => a.score - b.score)[0]

  const byForce: Record<Force, { title: string; text: string }> = {
    push: {
      title: 'Сузить до горящего сегмента',
      text: `Найди нишу, где проблему из «${short}» терпят ежедневно и она стоит им денег. Узкий сегмент с острой болью купит быстрее, чем «все понемногу».`,
    },
    pull: {
      title: 'Продавать результат, а не процесс',
      text: `Переформулируй «${short}» через измеримый итог: цифра, срок, экономия. Клиент должен увидеть себя «после» за одну фразу — тогда появляется тяга.`,
    },
    inertia: {
      title: 'Убрать трение на входе',
      text: `Сделай так, чтобы «${short}» внедрялось без миграции и обучения: интеграция в текущий инструмент, бесплатный пробный режим, откат в один клик. Снизишь страх — поднимешь конверсию.`,
    },
  }

  const variants = [
    byForce[weakest.force],
    {
      title: 'Сменить покупателя',
      text: `Возможно, «${short}» нужно не пользователю, а тому, кто страдает от его бездействия (руководитель, финдир). Смена ЛПР часто превращает «слабо» в «золото».`,
    },
    {
      title: 'Урезать до одного действия',
      text: `Выкини всё, кроме одной самой ценной функции «${short}». Один наконечник, который бьёт точно в боль, продаётся лучше швейцарского ножа.`,
    },
  ]

  return delay({ targetScore: 5.0, variants }, 1000)
}

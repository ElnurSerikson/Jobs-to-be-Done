// ───────────────────────── seeded RNG (детерминизм по тексту идеи) ─────────────────────────

function hashString(str: string): number {
  let h = 2166136261
  for (let i = 0; i < str.length; i++) {
    h ^= str.charCodeAt(i)
    h = Math.imul(h, 16777619)
  }
  return h >>> 0
}

function mulberry32(seed: number): () => number {
  let a = seed
  return () => {
    a |= 0
    a = (a + 0x6d2b79f5) | 0
    let t = Math.imul(a ^ (a >>> 15), 1 | a)
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296
  }
}

function pick<T>(rng: () => number, arr: T[]): T {
  return arr[Math.floor(rng() * arr.length)]
}

// score 1–5: low (1–2), mid (3), high (4–5)
function tier(score: number): 'low' | 'mid' | 'high' {
  if (score <= 2) return 'low'
  if (score === 3) return 'mid'
  return 'high'
}

function shortIdea(idea: string): string {
  const clean = idea.trim().replace(/\s+/g, ' ')
  if (clean.length <= 90) return clean
  return clean.slice(0, 87).trimEnd() + '…'
}

